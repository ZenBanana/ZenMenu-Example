import Foundation
import UIKit

extension UIColor {
    
    class func ZBLightBlue() -> UIColor {
        return UIColor(red: 134.0/255.0, green: 211.0/255.0, blue: 237.0/255.0, alpha: 1.0)
    }
    
    class func ZBMediumBlue() -> UIColor {
        return UIColor(red: 35.0/255.0, green: 145.0/255.0, blue: 204.0/255.0, alpha: 1.0)
    }
    
    class func ZBDarkBlue() -> UIColor {
        return UIColor(red: 20.0/255.0, green: 40.0/255.0, blue: 70.0/255.0, alpha: 1.0)
    }
    
    class func ZBRed() -> UIColor {
        return UIColor(red: 236.0/255.0, green: 41.0/255.0, blue: 45.0/255.0, alpha: 1.0)
    }
    
    class func ZBYellow() -> UIColor {
        return UIColor(red: 254.0/255.0, green: 196.0/255.0, blue: 39.0/255.0, alpha: 1.0)
    }
    
}
